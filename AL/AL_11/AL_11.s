	beq	space
	cmp.b	#".",d0
	beq	point
	cmp.b	#"-",d0
	beq	minus
	cmp.b	#"?",d0
	beq	interrog
	cmp.b	#":",d0
	beq	dblpt
	sub.b	#"A",d0
	lea	fonts,a0
	lea	fonts2,a1
	lsl.w	#5,d0
	mulu	#41,d0
	add.l	d0,a0
	add.l	d0,a1
