scroller:
	addi.w	#1,pas
	cmpi.w	#4,pas
	bne.s	.no_decalage
	move.w	#0,pas
	bsr.s	next_carac
.no_decalage:
	bsr	scrollY
	bra	bendbuff
reset:
	move.l	#texte,ptr_texte
next_carac:
	movea.l	ptr_texte,a0
	move.b	(a0)+,d0
	move.l	a0,ptr_texte
	cmpi.b	#-1,d0
	beq.s	reset
* 0 à 9
	cmpi.b	#"0",d0
	bne.s	.1
	move.b	#30,d0	
	bra	funt
.1:
	cmpi.b	#"1",d0
	bne.s	.2
	move.b	#31,d0	
	bra.s	funt
.2:
	cmpi.b	#"2",d0
	bne.s	.3
	move.b	#32,d0	
	bra.s	funt
.3:
	cmpi.b	#"3",d0
	bne.s	.4
	move.b	#33,d0	
	bra.s	funt
.4:
	cmpi.b	#"4",d0
	bne.s	.5
	move.b	#34,d0	
	bra.s	funt
.5:
	cmpi.b	#"5",d0
	bne.s	.6
	move.b	#35,d0	
	bra.s	funt
.6:
	cmpi.b	#"6",d0
	bne.s	.7
	move.b	#36,d0	
	bra.s	funt
.7:
	cmpi.b	#"7",d0
	bne.s	.8
	move.b	#37,d0	
	bra.s	funt
.8:
	cmpi.b	#"8",d0
	bne.s	.9
	move.b	#38,d0	
	bra.s	funt
.9:
	cmpi.b	#"9",d0
	bne.s	.nop
	move.b	#39,d0	
	bra.s	funt
.nop:
	cmpi.b	#".",d0
	bne.s	point
	move.b	#26,d0	
	bra.s	funt
point:
	cmpi.b	#" ",d0
	bne.s	space
	move.b	#27,d0	
	bra.s	funt
space:
	subi.b	#"A",d0
funt:
	andi.w	#$ff,d0
	muls	#4*32,d0
	lea	font,a0
	adda.w	d0,a0
	lea	puffer+40,a1
	move.w	#31,d0
.loop:
	move.w	(a0)+,00(a1)
	move.w	(a0)+,02(a1)
	lea	6*8(a1),a1
	dbf	d0,.loop
	rts

scrollY:
	lea	puffer,a0
	move.w	#31,d0
scrl1
i set 1
 rept 44
	move.b	i(a0),i-1(a0)
i set i+1
 endr
	lea	6*8(a0),a0
	dbf	d0,scrl1
	rts

bendbuff:
	cmpi.w	#0,counter
	bne.s	.go_away
	move.w	#720,counter
.go_away:
	subi.w	#4,counter
	lea	wave,a2
	adda.w	counter,a2
	lea	auto_generate(pc),a0
	moveq	#19,d7
	move.w	#76,d2
	moveq	#0,d3
	moveq	#0,d4
.loop:
	move.w	d2,d0
	move.w	d2,d1
	add.w	(a2)+,d0
	add.w	(a2)+,d1
	muls	#160,d0
	muls	#160,d1
	addq.w	#1,d1
	add.w	d3,d0
	add.w	d3,d1
	move.w	d4,2(a0)
	move.w	d4,8(a0)
	addq.w	#1,8(a0)
	move.w	d0,4(a0)	
	move.w	d1,10(a0)	
	addq.w	#8,d3
	addq.w	#2,d4
	lea	12(a0),a0
	dbf	d7,.loop
	movea.l	Zorro_scr1,a0
	addq.w	#PLAN_SCROLLER,a0
	adda.l	#160*20,a0
	lea	blnk,a1
	move.w	#32,d0
auto_generate:
 rept 40
	move.b	0(a1),0(a0)
 endr
	lea	6*8(a1),a1
	lea	160(a0),a0
	dbf	d0,auto_generate
	rts

init_scroller:
	lea	font,a0	get start of font
	lea	fonte,a1	get start of picture
	moveq	#0,d1		y=0
big_loop:
	moveq	#0,d0		x=0
.loop:
	movea.l	a1,a2
	move.w	d0,d2
	muls	#16,d2
	adda.l	d2,a2
	move.w	d1,d2
	muls	#160*32,d2
	adda.l	d2,a2
i	set	0
 rept	32
	move.w	i*160+0(a2),(a0)+
	move.w	i*160+8(a2),(a0)+
i	set	i+1
 endr
	addq.w	#1,d0
	cmpi.w	#10,d0
	bne	.loop
	addq.w	#1,d1
	cmpi.w	#3+1,d1
	bne	big_loop
	rts

fadein:
	move.l	#$777,d0
deg	
	bsr.s	wart
	bsr.s	wart
	bsr.s	wart
	lea	$ffff8240.w,a0
	moveq	#15,d1
chg1	
	move.w	d0,(a0)+
	dbf	d1,chg1
	sub.w	#$111,d0
	bne.s	deg
	rts

wart:
	move.l	d0,-(sp)
	move.l	$466.w,d0
att	
	cmp.l	$466.w,d0
	beq.s	att
	move.l	(sp)+,d0
	rts
				
